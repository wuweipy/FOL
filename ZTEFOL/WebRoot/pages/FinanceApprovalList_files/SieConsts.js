  //根据不同语言环境得到相应code的信息。没有替换文字功能。
  function getSieMessage(code,culture) {
    var postfix = "_zh_CN";
    if(culture == "zh-CN")
    {
        postfix = "_zh_CN";
    }
    else
    {
        postfix = "_en_US";
    }
    var retval = "";
    try{ retval = eval(code+postfix);}
    catch(e){retval=code;}
	return retval;
  }


