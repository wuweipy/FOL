package Data.UserDetail;

public class DUserDetail {

	private int id;
	
	private String no;
	
	private String allNo;
	
	private int deptID;
	
	private String email;
	
	private String tel;
	
	private int productNo;
	
	private int techTitleNo;
	
	public DUserDetail()
	{
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getAllNo() {
		return allNo;
	}

	public void setAllNo(String allNo) {
		this.allNo = allNo;
	}

	public int getDeptID() {
		return deptID;
	}

	public void setDeptID(int deptID) {
		this.deptID = deptID;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public int getProductNo() {
		return productNo;
	}

	public void setProductNo(int productNo) {
		this.productNo = productNo;
	}

	public int getTechTitleNo() {
		return techTitleNo;
	}

	public void setTechTitleNo(int techTitleNo) {
		this.techTitleNo = techTitleNo;
	}
	
	
}
